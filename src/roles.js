const sections = Object.keys(section)
const roleMap = sections.map(s => Object.keys(section[s].role).map((r, i) => ({ section: s, role: r }))).flat()

function roles(sec) {
    if (!sec || sec === 'None' || sec === 'Section') {
        let rs = []
        sections.forEach(s => {
            rs = rs.concat(Object.keys(section[s].role))
        })
        return rs
    }
    else return Object.keys(section[sec].role)
}

function options(el, opts) {
    el.innerHTML = ''
    opts.forEach(t => {
        let o = document.createElement('option');
        o.text = t
        el.add(o)
    })
}

function selectSection(p, sn, rn) {
    const r = p.fe(rn),
        s = p.fe(sn)
    options(r, ['Role'].concat(roles(s.value)))
    r.value = 'Role'
}
function selectRole(p, sn, rn) {
    const s = p.fe(sn)
    if (s.value === 'Section') {
        const r = p.fe(rn),
            v = r.value,
            i = r.selectedIndex,
            so = roleMap[i - 1]
        s.value = so.section
        options(r, ['Role'].concat(roles(so.section)))
        r.value = v
    }
}
export { section, sections, roles, selectSection, selectRole }